#include "libft.h"

int	ft_strlcat(char *dst, const char *src, int size)
{
	int	i;
	int	j;

	if (size < 0)
		size = ft_strlen(dst) + ft_strlen(src) + 1 + ((size + 1) % size);
	i = 0;
	j = 0;
	while (dst[i] && i < size)
		i++;
	while (src[j] && (i + j + 1) < size)
	{
		dst[i + j] = src[j];
		j++;
	}
	if (i < size)
		dst[i + j] = '\0';
	return (i + ft_strlen(src));
}
