#include "libft.h"

int	ft_strlcpy(char *dst, const char *src, int size)
{
	int	i;

	if (size == 0)
		return (ft_strlen(src));
	if (size < 0)
		size = ft_strlen(src) + 1 + ((size + 1) % size);
	i = 0;
	while (src[i] && (i < size - 1))
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (ft_strlen(src));
}
