char	*ft_strrchr(const char *s, int c)
{
	char	*s1;

	s1 = (char *) s;
	if (c > 127)
		c %= 256;
	while (*s1)
		s1++;
	if (c == *s1)
		return (s1);
	else
		s1--;
	while (*s1)
	{
		if (c == *s1)
			return (s1);
		s1--;
	}
	return (0);
}
